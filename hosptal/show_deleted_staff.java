    private void query() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try {
            String today = "";
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/hospital", "root", "dps123");

            String SQLa = "Select staff_id,name,fathers_name,age,department,contact_no,gender,address,curdate() from staff where status='" + ("N") + "'";

            ResultSet rs2 = con.createStatement().executeQuery(SQLa);
            while (rs2.next()) {
                String pid = rs2.getString("staff_id");
                String pname = rs2.getString("name");
                String pfather = rs2.getString("fathers_name");
                int page = rs2.getInt("age");
                String pdepartment = rs2.getString("department");
                 int pcontact = rs2.getInt("contact_no");
                String pgender = rs2.getString("gender");

                String paddress = rs2.getString("address");
                today=rs2.getString("curdate()");
                model.addRow(new Object[]{pid, pname, pfather, page, pdepartment, pcontact,pgender, paddress});
            }
            jLabel2.setText("Today is: " + today);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

